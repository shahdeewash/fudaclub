// Copy mapped images into client/public/menu/ and generate the UPDATE SQL
// statement that Railway MySQL will run to populate menuItems.imageUrl.
//
// Filenames are sanitized (no spaces) so they're URL-safe and Vite can serve
// them at /menu/<filename>. The DB stores the same path the browser will
// fetch — `imageUrl = "/menu/<sanitized-filename>"`.
const fs = require("fs");
const path = require("path");

const SRC_DIR = "C:\\Users\\dee_s\\OneDrive\\Documents\\FÜDA\\FÜDA GLOBAL STREET BITES\\Fuda Food photo shoot\\doordash photo shoot\\fuda-menu-images";
const DST_DIR = path.resolve(__dirname, "..", "client", "public", "menu");

const mapping = JSON.parse(fs.readFileSync(path.resolve(__dirname, "image-mapping.json"), "utf8"));

if (!fs.existsSync(DST_DIR)) {
  fs.mkdirSync(DST_DIR, { recursive: true });
  console.log(`Created ${DST_DIR}`);
}

// Sanitize filenames — keep them lowercased, alphanumerics + dashes, preserve
// extension. Avoids %20 escaping in URLs and any locale weirdness.
function sanitize(filename) {
  const ext = path.extname(filename);
  const base = path.basename(filename, ext)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
  return `${base}${ext.toLowerCase()}`;
}

// Set of unique source files (multiple menu items may share the same image)
const uniqueFiles = new Set(mapping.map(m => m.file));
const fileMap = new Map(); // original filename → sanitized filename

console.log(`Copying ${uniqueFiles.size} unique images to ${DST_DIR}\n`);

let copied = 0;
let skipped = 0;
for (const f of uniqueFiles) {
  const sanitized = sanitize(f);
  fileMap.set(f, sanitized);
  const src = path.join(SRC_DIR, f);
  const dst = path.join(DST_DIR, sanitized);
  if (fs.existsSync(dst)) {
    skipped++;
    continue;
  }
  fs.copyFileSync(src, dst);
  copied++;
}
console.log(`  Copied: ${copied}`);
console.log(`  Already existed: ${skipped}\n`);

// Generate ONE big UPDATE with a CASE expression covering every item. Railway's
// MySQL Data tab runs only the first statement on each ctrl+Return; sending 55+
// separate UPDATEs would mean 55+ round trips through the UI. CASE collapses
// it to a single round trip.
const allIds = mapping.map(m => m.id).sort((a, b) => a - b);

let sql = "";
sql += "-- Auto-generated by scripts/copy-and-gen-sql.cjs.\n";
sql += "-- Single UPDATE that maps menu item rows to /menu/<filename>.jpg URLs.\n";
sql += "-- Images live in client/public/menu/ (committed in this PR) so they're\n";
sql += "-- served statically at the corresponding paths.\n";
sql += "--\n";
sql += "-- Idempotent: re-running just overwrites with the same value.\n";
sql += "-- Square sync preserves imageUrl on update when Square has no image\n";
sql += "-- (the current case for every item) — so these survive re-syncs.\n";
sql += "\n";
sql += "UPDATE menuItems SET imageUrl = CASE\n";
for (const m of mapping.sort((a, b) => a.id - b.id)) {
  const url = `/menu/${fileMap.get(m.file)}`;
  // Strip $ from comments so Railway's bash-shelled query box doesn't try to
  // interpret e.g. "$22.99" as a positional-param reference. Backticks are
  // similarly unsafe (we don't use any).
  const safeName = m.name.replace(/'/g, "''").replace(/\$/g, "");
  sql += `  WHEN id = ${m.id} THEN '${url}'  -- ${safeName}\n`;
}
sql += "  ELSE imageUrl\n";
sql += "END\n";
sql += `WHERE id IN (${allIds.join(", ")});\n`;

fs.writeFileSync(path.resolve(__dirname, "set-image-urls.sql"), sql);
console.log(`Wrote scripts/set-image-urls.sql (1 statement covering ${mapping.length} items)`);
